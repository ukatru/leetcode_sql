with t1 as (select user_id,count(action) total_actions from confirmations
group by user_id),
t2 as (
select user_id,count(action)*1.0 confirmed_actions from confirmations
where action = 'confirmed'
group by user_id 
),
t3 as (
select t1.user_id,COALESCE(t2.confirmed_actions,0)/t1.total_actions confirmation_rate
from t1 left join t2
on t1.user_id = t2.user_id)
select s.user_id,cast(COALESCE(confirmation_rate,0) as decimal(4,2)) confirmation_rate from signups s left join t3
on s.user_id = t3.user_id


select s.user_id, 
       ISNULL(round(sum(case when action = 'confirmed' then 1 else 0 end) * 1.0 / NULLIF(count(*),0) ,2), 0.00) as confirmation_rate
       from Signups s 
       left join Confirmations c on c.user_id  = s.user_id 
group by s.user_id 